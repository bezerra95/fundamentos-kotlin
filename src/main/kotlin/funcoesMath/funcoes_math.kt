package funcoesMath

fun main() {
    val numero = 4.5

    // Funções matemáticas
    println(Math.abs(-7)) // Valor absoluto: 7
    println(Math.sqrt(25.0)) // Raiz quadrada: 5.0
    println(Math.pow(2.0, 3.0)) // Potência: 8.0
    println(Math.max(10, 15)) // Maior valor: 15
    println(Math.min(10, 15)) // Menor valor: 10
    println(Math.ceil(numero)) // Teto: 5.0
    println(Math.floor(numero)) // Chão: 4.0
    println(Math.round(numero)) // Arredondamento: 5
    println(Math.exp(1.0)) // Exponencial: 2.718281828459045
    println(Math.log(2.718281828459045)) // Logaritmo natural (base e): 1.0
    println(Math.log10(100.0)) // Logaritmo na base 10: 2.0
    println(Math.sin(0.0)) // Seno: 0.0
    println(Math.cos(Math.PI)) // Cosseno: -1.0
    println(Math.tan(0.0)) // Tangente: 0.0
    println(Math.asin(0.0)) // Arco seno: 0.0
    println(Math.acos(-1.0)) // Arco cosseno: 3.141592653589793
    println(Math.atan(1.0)) // Arco tangente: 0.7853981633974483
}